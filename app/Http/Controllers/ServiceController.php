     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(ServiceForm $request)
    {
        Service::insert([
            'icon' => $request->icon,
            'title' => $request->title,
            'description' => $request->description,
            'created_by' => Auth::id(),
            'created_at' => Carbon::now(),
        ]);

        return Redirect::to('service')->with('success', 'Service Add Successfully!!');
    }

